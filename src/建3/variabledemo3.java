package 建3;
public class variabledemo3{
	public static void main (String[] args) {
	//byte
	byte b=10;
	System.out.println(b);
	//short
	short s=20;
	System.out.println(s);
	//int
	int i=30;System.out.println(i);
	//long
	//如果要定义long类型的变量
	//在数据值后面需要家一个L作为后缀
	long h=99999999L;
	System.out.println(h);
	//float
	//注意点:定义float类型变量的时候
	//数据值也需要加一个F作为后缀
	float f=10.1F;
	System.out.println(f);
	
	//boolean
	//ture false
	boolean o=true;
	System.out.println(s);
	}
}