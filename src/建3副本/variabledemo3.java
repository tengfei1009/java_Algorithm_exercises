package 建3副本;
public class variabledemo3{
	public static void main (String[] args) {
	//byte
	byte b=10;
	System.out.println(b);
	//short
	short s=20;
	System.out.println(s);
	//int
	int i=30;System.out.println(i);
	//long
	long h=99999999L;
	System.out.println(h);
	//float
	float f=10.1F;
	System.out.println(f);
	
	//boolean
	//true false
	boolean o=true;
	System.out.println(o);
	}
}